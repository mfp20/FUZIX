/*
 *	Generate the syscall functions
 *
 *	68HC11 via SWI
 *
 *	On return X holds errno if there is one, D holds the return value
 */

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include "syscall_name.h"

static char namebuf[128];

static void write_call(int n)
{
  FILE *fp;
  snprintf(namebuf, 128, "fuzix68hc11/syscall_%s.s",syscall_name[n]);
  fp = fopen(namebuf, "w");
  if (fp == NULL) {
    perror(namebuf);
    exit(1);
  }
  fprintf(fp, "\t.text\n\n");
  fprintf(fp, "\t.globl %s\n\n", syscall_name[n]);
  fprintf(fp, "%s:\n", syscall_name[n]);
  fprintf(fp, "\tpshb\n");
  fprintf(fp, "\tpsha\n");
  fprintf(fp, "\tldd #%d\n", 0x80 | (n << 8));
  fprintf(fp, "\tjsr __sysvector\n");
  fprintf(fp, "\tcpx #0\n");
  fprintf(fp, "\tbeq noerror\n");
  fprintf(fp, "\tstx errno\n");
  fprintf(fp, "noerror:\n");
  fprintf(fp, "\tpulx\n");	/* Remove saved parameter */
  fprintf(fp, "\trts\n");
  fclose(fp);
}

/* In the varargs case B already holds the argument count info */
static void write_vacall(int n)
{
  FILE *fp;
  snprintf(namebuf, 128, "fuzix68hc11/syscall_%s.s",syscall_name[n]);
  fp = fopen(namebuf, "w");
  if (fp == NULL) {
    perror(namebuf);
    exit(1);
  }
  /* This should work the same as we have 2 fixed args */
  fprintf(fp, "\t.text\n\n");
  fprintf(fp, "\t.globl %s\n\n", syscall_name[n]);
  fprintf(fp, "%s:\n", syscall_name[n]);
  fprintf(fp, "\tpshb\n");
  fprintf(fp, "\tpsha\n");
  fprintf(fp, "\tldd #%d\n", 0x80 | (n << 8));
  fprintf(fp, "\tjsr __sysvector\n");
  fprintf(fp, "\tcpx #0\n");
  fprintf(fp, "\tbeq noerror\n");
  fprintf(fp, "\tstx errno\n");
  fprintf(fp, "noerror:\n");
  fprintf(fp, "\tpulx\n");	/* Remove saved parameter */
  fprintf(fp, "\trts\n");
  fclose(fp);
}

static void write_call_table(void)
{
  int i;
  for (i = 0; i < NR_SYSCALL; i++)
    if (syscall_args[i] == VARARGS)
      write_vacall(i);
    else
      write_call(i);
}

static void write_makefile(void)
{
  int i;
  char path[256];
  FILE *fp;

  fp = fopen("fuzix68hc11/Makefile", "w");
  if (fp == NULL) {
    perror("Makefile");
    exit(1);
  }
  fprintf(fp, "# Autogenerated by tools/syscall68hc11\n");
  fprintf(fp, "ASRCS = syscall_%s.s\n", syscall_name[0]);
  for (i = 1; i < NR_SYSCALL; i++)
    fprintf(fp, "ASRCS += syscall_%s.s\n", syscall_name[i]);
  fprintf(fp, "\n\nASRCALL = $(ASRCS)\n");
  fprintf(fp, "\nAOBJS = $(ASRCALL:.s=.o)\n\n");
  fprintf(fp, "../syslib.lib: $(AOBJS)\n");
  fprintf(fp, "\techo $(AOBJS) >syslib.l\n");
  fprintf(fp, "\tar rc ../syslib.lib $(AOBJS)\n\n");
  fprintf(fp, "$(AOBJS): %%.o: %%.s\n");
  fprintf(fp, "\tm6811-elf-gcc -mshort -mrelax -c $<\n\n");
  fprintf(fp, "clean:\n");
  fprintf(fp, "\trm -f $(AOBJS) $(ASRCS) *~\n\n");
  fclose(fp);
}

int main(int argc, char *argv[])
{
  write_makefile();
  write_call_table();
  exit(0);
}
